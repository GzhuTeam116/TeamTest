package com.bookstore.data;

public class OrderFormDetailInfo {

	private String orderform_bookname ;
	private String orderform_bookprice ;
	private String orderform_bookcount ;
	private String orderform_booktotal ;
	private String orderformdetail_img ;
	public String getOrderform_bookname() {
		return orderform_bookname;
	}
	public void setOrderform_bookname(String orderform_bookname) {
		this.orderform_bookname = orderform_bookname;
	}
	public String getOrderform_bookprice() {
		return orderform_bookprice;
	}
	public void setOrderform_bookprice(String orderform_bookprice) {
		this.orderform_bookprice = orderform_bookprice;
	}
	public String getOrderform_bookcount() {
		return orderform_bookcount;
	}
	public void setOrderform_bookcount(String orderform_bookcount) {
		this.orderform_bookcount = orderform_bookcount;
	}
	public String getOrderform_booktotal() {
		return orderform_booktotal;
	}
	public void setOrderform_booktotal(String orderform_booktotal) {
		this.orderform_booktotal = orderform_booktotal;
	}
	public String getOrderformdetail_img() {
		return orderformdetail_img;
	}
	public void setOrderformdetail_img(String orderformdetail_img) {
		this.orderformdetail_img = orderformdetail_img;
	}
	
	
}
